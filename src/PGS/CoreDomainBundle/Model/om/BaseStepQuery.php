     *
     * @return                 StepQuery The current query, for fluid interface
     * @throws PropelException - if the provided filter is invalid.
     */
    public function filterByStepI18n($stepI18n, $comparison = null)
    {
        if ($stepI18n instanceof StepI18n) {
            return $this
                ->addUsingAlias(StepPeer::ID, $stepI18n->getId(), $comparison);
        } elseif ($stepI18n instanceof PropelObjectCollection) {
            return $this
                ->useStepI18nQuery()
                ->filterByPrimaryKeys($stepI18n->getPrimaryKeys())
                ->endUse();
        } else {
            throw new PropelException('filterByStepI18n() only accepts arguments of type StepI18n or PropelCollection');
        }
    }

    /**
     * Adds a JOIN clause to the query using the StepI18n relation
     *
     * @param     string $relationAlias optional alias for the relation
     * @param     string $joinType Accepted values are null, 'left join', 'right join', 'inner join'
     *
     * @return StepQuery The current query, for fluid interface
     */
    public function joinStepI18n($relationAlias = null, $joinType = 'LEFT JOIN')
    {
        $tableMap = $this->getTableMap();
        $relationMap = $tableMap->getRelation('StepI18n');

        // create a ModelJoin object for this join
        $join = new ModelJoin();
        $join->setJoinType($joinType);
        $join->setRelationMap($relationMap, $this->useAliasInSQL ? $this->getModelAlias() : null, $relationAlias);
        if ($previousJoin = $this->getPreviousJoin()) {
            $join->setPreviousJoin($previousJoin);
        }

        // add the ModelJoin to the current object
        if ($relationAlias) {
            $this->addAlias($relationAlias, $relationMap->getRightTable()->getName());
            $this->addJoinObject($join, $relationAlias);
        } else {
            $this->addJoinObject($join, 'StepI18n');
        }

        return $this;
    }

    /**
     * Use the StepI18n relation StepI18n object
     *
     * @see       useQuery()
     *
     * @param     string $relationAlias optional alias for the relation,
     *                                   to be used as main alias in the secondary query
     * @param     string $joinType Accepted values are null, 'left join', 'right join', 'inner join'
     *
     * @return   \PGS\CoreDomainBundle\Model\StepI18nQuery A secondary query class using the current class as primary query
     */
    public function useStepI18nQuery($relationAlias = null, $joinType = 'LEFT JOIN')
    {
        return $this
            ->joinStepI18n($relationAlias, $joinType)
            ->useQuery($relationAlias ? $relationAlias : 'StepI18n', '\PGS\CoreDomainBundle\Model\StepI18nQuery');
    }

