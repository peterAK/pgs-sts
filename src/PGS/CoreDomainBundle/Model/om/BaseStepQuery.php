            if (isset($id['max'])) {
                $this->addUsingAlias(StepPeer::ID, $id['max'], Criteria::LESS_EQUAL);
                $useMinMax = true;
            }
            if ($useMinMax) {
                return $this;
            }
            if (null === $comparison) {
                $comparison = Criteria::IN;
            }
        }

        return $this->addUsingAlias(StepPeer::ID, $id, $comparison);
    }

