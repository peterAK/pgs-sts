                $translation = ProcessI18nQuery::create()
                    ->filterByPrimaryKey(array($this->getPrimaryKey(), $locale))
                    ->findOneOrCreate($con);
                $this->currentTranslations[$locale] = $translation;
            }
            $this->addProcessI18n($translation);
        }

        return $this->currentTranslations[$locale];
    }

