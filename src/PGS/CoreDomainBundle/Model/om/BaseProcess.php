        $this->processI18nsScheduledForDeletion = clone $processI18nsToDelete;

        foreach ($processI18nsToDelete as $processI18nRemoved) {
            $processI18nRemoved->setProcess(null);
        }

        $this->collProcessI18ns = null;
        foreach ($processI18ns as $processI18n) {
            $this->addProcessI18n($processI18n);
        }

        $this->collProcessI18ns = $processI18ns;
        $this->collProcessI18nsPartial = false;

        return $this;
