    {
        $partial = $this->collProcessI18nsPartial && !$this->isNew();
        if (null === $this->collProcessI18ns || null !== $criteria  || $partial) {
            if ($this->isNew() && null === $this->collProcessI18ns) {
                // return empty collection
                $this->initProcessI18ns();
            } else {
                $collProcessI18ns = ProcessI18nQuery::create(null, $criteria)
                    ->filterByProcess($this)
                    ->find($con);
                if (null !== $criteria) {
                    if (false !== $this->collProcessI18nsPartial && count($collProcessI18ns)) {
                      $this->initProcessI18ns(false);

                      foreach ($collProcessI18ns as $obj) {
                        if (false == $this->collProcessI18ns->contains($obj)) {
                          $this->collProcessI18ns->append($obj);
                        }
                      }

                      $this->collProcessI18nsPartial = true;
                    }

                    $collProcessI18ns->getInternalIterator()->rewind();

                    return $collProcessI18ns;
