            $this->doAddProcessI18n($l);

            if ($this->processI18nsScheduledForDeletion and $this->processI18nsScheduledForDeletion->contains($l)) {
                $this->processI18nsScheduledForDeletion->remove($this->processI18nsScheduledForDeletion->search($l));
